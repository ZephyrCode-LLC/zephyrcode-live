/**
 * Emits supabase/seed.sql from scripts/content — the same data seed.ts uses,
 * as idempotent SQL (used to seed via the Supabase MCP, which has no service key).
 *   pnpm tsx scripts/emit-seed-sql.mjs
 */
import { writeFileSync } from "node:fs";
import { ALL_SITES } from "./content/index.ts";

const q = (v) => {
  if (v === null || v === undefined) return "null";
  if (typeof v === "number") return String(v);
  if (typeof v === "boolean") return v ? "true" : "false";
  if (typeof v === "object") return `'${JSON.stringify(v).replace(/'/g, "''")}'::jsonb`;
  return `'${String(v).replace(/'/g, "''")}'`;
};

let sql = "-- generated by scripts/emit-seed-sql.mjs — do not edit by hand\nbegin;\n";
const onceTruncated = new Set();
const trunc = (t) => {
  if (!onceTruncated.has(t)) {
    sql += `delete from ${t};\n`;
    onceTruncated.add(t);
  }
};

for (const seed of ALL_SITES) {
  const s = seed.site;
  sql += `insert into sites (slug, host, title, description, accent, og_image, nav_order) values (${q(s.slug)}, ${q(s.host)}, ${q(s.title)}, ${q(s.description)}, ${q(s.accent)}, ${q(s.og_image)}, ${q(s.nav_order)}) on conflict (slug) do update set host=excluded.host, title=excluded.title, description=excluded.description, accent=excluded.accent, og_image=excluded.og_image, nav_order=excluded.nav_order;\n`;
  sql += `delete from blocks where site_slug = ${q(s.slug)};\n`;
  for (const b of seed.blocks) {
    sql += `insert into blocks (site_slug, section, kind, position, data) values (${q(s.slug)}, ${q(b.section)}, ${q(b.kind)}, ${q(b.position)}, ${q(b.data)});\n`;
  }
  if (seed.books) {
    trunc("books");
    for (const b of seed.books)
      sql += `insert into books (position, title, author, pos, register, lang, note) values (${q(b.position)}, ${q(b.title)}, ${q(b.author)}, ${q(b.pos)}, ${q(b.register)}, ${q(b.lang)}, ${q(b.note)});\n`;
  }
  if (seed.watch) {
    trunc("watch_titles");
    trunc("watch_channels");
    for (const c of seed.watch) {
      sql += `with ch as (insert into watch_channels (n, name, tagline, position) values (${q(c.n)}, ${q(c.name)}, ${q(c.tagline)}, ${q(c.position)}) returning id)\n`;
      sql += `insert into watch_titles (channel_id, title, match, platform, why, position) select ch.id, t.* from ch, (values ${c.titles.map((t) => `(${q(t.title)}, ${q(t.match)}::int, ${q(t.platform)}, ${q(t.why)}, ${q(t.position)}::int)`).join(", ")}) as t(title, match, platform, why, position);\n`;
    }
  }
  if (seed.listenProofs) {
    trunc("listen_tracks");
    trunc("listen_proofs");
    for (const p of seed.listenProofs) {
      sql += `with pf as (insert into listen_proofs (n, name, claim, voices, position) values (${q(p.n)}, ${q(p.name)}, ${q(p.claim)}, ${q(p.voices)}, ${q(p.position)}) returning id)\n`;
      sql += `insert into listen_tracks (proof_id, title, artist, why, position) select pf.id, t.* from pf, (values ${p.tracks.map((t) => `(${q(t.title)}, ${q(t.artist)}, ${q(t.why)}, ${q(t.position)}::int)`).join(", ")}) as t(title, artist, why, position);\n`;
    }
  }
  if (seed.sims) {
    trunc("sims");
    for (const m of seed.sims)
      sql += `insert into sims (slug, name, grammar, blurb, position) values (${q(m.slug)}, ${q(m.name)}, ${q(m.grammar)}, ${q(m.blurb)}, ${q(m.position)});\n`;
  }
  if (seed.chapters) {
    trunc("chapters");
    for (const c of seed.chapters)
      sql += `insert into chapters (code, dateline, title, sealed, position) values (${q(c.code)}, ${q(c.dateline)}, ${q(c.title)}, ${q(c.sealed)}, ${q(c.position)});\n`;
  }
  if (seed.dossiers) {
    trunc("dossiers");
    for (const d of seed.dossiers)
      sql += `insert into dossiers (role, name, blurb, tiers, position) values (${q(d.role)}, ${q(d.name)}, ${q(d.blurb)}, ${q(d.tiers)}, ${q(d.position)});\n`;
  }
  if (seed.storyShorts) {
    trunc("story_shorts");
    for (const st of seed.storyShorts)
      sql += `insert into story_shorts (title, slug, status, featured, body, markdown, position) values (${q(st.title)}, ${q(st.slug ?? null)}, ${q(st.status)}, ${q(st.featured)}, ${q(st.body)}, ${q(st.markdown ?? null)}, ${q(st.position)});\n`;
  }
}
sql += "commit;\n";
writeFileSync("supabase/seed.sql", sql);
console.log("wrote supabase/seed.sql", sql.length, "bytes");
